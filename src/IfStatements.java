public class IfStatements {

    public static void main(String[] args) {

        int age = 12;

        if (age>=75) {
            System.out.println("Ok Boomer!");
        }
        else if (age>=18) {
            System.out.println("You are an adult!");
        }
        else if (age>=13) {
            System.out.println("You are teenager!");
        }
        else {
            System.out.println("You are a kid!");
        }
    }
}